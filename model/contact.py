class Contact:

    def __init__(self, first_name=None, last_name=None, address=None, home_phone=None, mobile_phone=None, email=None):
        self.first_name = first_name
        self.last_name = last_name
        self.address = address
        self.home_phone = home_phone
        self.mobile_phone = mobile_phone
        self.email = email
